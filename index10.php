}

function KeyCreate(tmp) {
	var out = '';
	var cnt = tmp.length;
	for (var j = 0; j < cnt; j++) {
		tt = Math.floor((tmp.length - 1) * Math.random());
		out += tmp.charAt(tt);
		tmp = tmp.substring(0, tt).concat(tmp.substring(tt + 1));
	}
	return out;
}

